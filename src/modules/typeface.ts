export abstract class Typeface {
    public static readonly Brand         = `Roboto`
    public static readonly Plain         = `Roboto`
    public static readonly WeightBold    = `700`
    public static readonly WeightMedium  = `500`
    public static readonly WeightRegular = `400`

    public static readonly Number = class {
        public static readonly WeightBold    = 700
        public static readonly WeightMedium  = 500
        public static readonly WeightRegular = 400
    }
} 
